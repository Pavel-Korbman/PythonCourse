def select(func, col):
    return [func(x) for x in col]  # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –∏–∑ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ func


def where(func, col):
    return [x for x in col if func(x)]  # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –∏–∑ –∑–Ω–∞—á–µ–Ω–∏–π, —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä—è—é—â–∏—Ö —É—Å–ª–æ–≤–∏—é –∏–∑ func


data = [1, 2, 3, 5, 8, 15, 23, 38]
res = select(int, data)  # –ü–µ—Ä–µ–≤–µ–ª–∏ –≤–µ—Å—å —Å–ø–∏—Å–æ–∫ –≤ —Ü–µ–ª—ã–µ —á–∏—Å–ª–∞
res = where(lambda x: x % 2 == 0, res) # –í—ã–±—Ä–∞–ª–∏ —Ç–æ–ª—å–∫–æ —á—ë—Ç–Ω—ã–µ
print(res)  # [2, 8, 38]
res = select(lambda x: (x, x**2), res)
# –ü—Ä–∏–º–µ–Ω–∏–ª–∏ lambda x: (x, x**2) (—Å–æ–∑–¥–∞–µ—Ç –∫–æ—Ä—Ç–µ–∂ –∏–∑ —á–∏—Å–ª–∞ –∏ –µ–≥–æ –∫–≤–∞–¥—Ä–∞—Ç–∞) –≤ —Ñ—É–Ω–∫—Ü–∏–∏ —Å–æ–∑–¥–∞–Ω–∏—è —Å–ø–∏—Å–∫–∞ select
print(res)  # [(2, 4), (8, 64), (38, 1444)]
